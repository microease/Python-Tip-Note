# 给你两个时间st和et(00:00:00<=st <= et<=23:59:59), 请你给出这两个时间间隔的秒数。
# 如：st="00:00:00", et="00:00:10", 则输出10.
st = "00:00:00"
et = "00:00:10"
# spit方法可以把数组分割
# map方法可以变成整数
# list方法变成数组
t1 = list(map(int, st.split(":")))
t2 = list(map(int, et.split(":")))
result = (t2[0] - t1[0]) * 3600 + (t2[1] - t1[1]) * 60 + (t2[2] - t1[2])
print(result)